//ejemplo de saludo dia de la semana

let dia = 'Miercoles';

switch (dia) {
    case 'lunes':
        alert('Hoy es lunes');
        break;
    case 'martes':
        alert('Hoy es martes');
        break;
    case 'miercoles':
        alert('Hoy es miecoles');
        break;
    case 'jueves':
        alert('Hoy es jueves');
        break;
    default:
        alert('No es un dia de la semana.');
        break;
}

